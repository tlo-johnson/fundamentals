char* fairRations(int num_people, int* loaves) {
  int odd_loaves = 0;
  for (int person = 0; person < num_people; person++) {
    if (loaves[person] % 2 == 1)
      odd_loaves++;
  }

  if (odd_loaves % 2 == 1)
    return "NO";

  int num_loaves = 0;
  for (int person = 0; person < num_people; person++) {
    if (loaves[person] % 2 == 0) continue;
    while (loaves[++person] % 2 == 0)
      num_loaves += 2;
    num_loaves += 2;
  }

  char* result = malloc(100 * sizeof(char));
  sprintf(result, "%d", num_loaves);
  return result;
}

